# try 문으로 코드를 감싼다 
# except, else, finally 를 모두 쓴 코드를 만든다 ! 
###예외처리

import sys

try: 
    x = int(input("숫자 입력해: "))
    print(x)
except: 
    print("숫자 아님 오류남 !!!!!!! ")
else: 
    print("숫자 맞음 오류 없다. ")
finally:
    print("END")


